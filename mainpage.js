function change_nav(index) {
  var number = index;
  switch (number) {
    case 1 : document.querySelector('.nav-part-mac').style.color = "#fff";
      break;
    case 2 : document.querySelector('.nav-part-ipad').style.color = "#fff";
      break;
    case 3 : document.querySelector('.nav-part-iphone').style.color = "#fff";
      break;
    case 4 : document.querySelector('.nav-part-watch').style.color = "#fff";
      break;
    case 5 : document.querySelector('.nav-part-music').style.color = "#fff";
      break;
    case 6 : document.querySelector('.nav-part-supportclient').style.color = "#fff";
      break;
  }
}

